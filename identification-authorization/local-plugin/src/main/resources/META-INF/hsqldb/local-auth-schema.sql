CREATE TABLE LOCAL_AUTH_TOKEN(
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  HIDDEN_ID VARCHAR(255),,
  VISUAL_ID VARCHAR(255),
  VALID BOOLEAN,
  CONSTRAINT LOCAL_TOKEN_HIDDEN_ID UNIQUE(HIDDEN_ID)
);

CREATE TABLE LOCAL_AUTH_CHARGING_STATION(
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  CHARGING_STATION_ID VARCHAR(255)
  CONSTRAINT CHARGING_STATION_CHARGING_STATION_ID UNIQUE(CHARGING_STATION_ID)
);

CREATE TABLE CHARGING_STATION_LOCAL_TOKEN(
  LOCAL_TOKEN_ID BIGINT,
  CHARGING_STATION_ID BIGINT,
  CONSTRAINT CHARGING_STATION_LOCAL_TOKEN_FK_LOCAL_TOKEN FOREIGN KEY(LOCAL_TOKEN_ID) REFERENCES PUBLIC.LOCAL_AUTH_TOKEN(ID),
  CONSTRAINT CHARGING_STATION_LOCAL_TOKEN_FK_CHARGING_STATION FOREIGN KEY(CHARGING_STATION_ID) REFERENCES PUBLIC.LOCAL_AUTH_CHARGING_STATION(ID)
)
